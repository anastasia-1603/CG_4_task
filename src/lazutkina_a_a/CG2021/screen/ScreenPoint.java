package lazutkina_a_a.CG2021.screen;

public class ScreenPoint {
    private int x, y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public ScreenPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
